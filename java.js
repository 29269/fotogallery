const kader = document.getElementById('container');

function fotoeen(url) {
    let item = document.createElement('div');
    item.className = 'item';
    let mijnFoto = document.createElement('img');
    mijnFoto.src = url;
    mijnFoto.alt = 'ventie';
    item.append(mijnFoto);
    kader.append(item);
}
